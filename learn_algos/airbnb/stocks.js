function main(input) {
	var lines = input.split('\n'),
		rows = lines.shift(),
		missings = [];

	function getValidLine(index, left, lines) {
		// search towards left/start of lines
		if (left) {
			while (index>0) {
				let val = lines[index].split(' ')[1].split('\t')[1];
				if (val.indexOf('Missing_') == -1) return index;
				index--;
			}
		} else {
			while (index<rows-1) {
				let val = lines[index].split(' ')[1].split('\t')[1]; 
				if (val.indexOf('Missing_') == -1) return index;
				index++;
			}
		}
		return index;
	}

	function getNum(n) {
		return parseFloat(lines[n].split(' ')[1].split('\t')[1]);
	}

	for (let i in lines) {
		i = parseInt(i);

		let line = lines[i],
			la = line.split(' '),
			value = la[1].split('\t')[1];
		if (value.indexOf('Missing_') != -1) {
			// calc slope from 1 behind and 1 ahead
			// if not possible to find 1 ahead, use both behind
			// if not possible to find 1 behind use both ahead
			let p = i-1, n = i+1, guess, prev, next, growth;
			if (p<=0) {
				p = getValidLine(n, false, lines);
				n = getValidLine(p+1, false, lines);
				prev = getNum(p);
				next = getNum(n);
				growth = next/prev;
				guess = prev/growth;
			} else if (n >= rows) {
				n = getValidLine(p, true, lines);
				p = getValidLine(n-1, true, lines);
				prev = getNum(p);
				next = getNum(n);
				growth = next/prev;
				guess = prev*growth;
			} else {
				p = getValidLine(p, true, lines);
				n = getValidLine(n, false, lines);
				prev = getNum(p);
				next = getNum(n);
				guess = (prev+next)/2;
			}
			missings.push(guess);

		}
	}
	return missings;
}



